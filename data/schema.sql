CREATE TABLE IF NOT EXISTS AQDevices(
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    AQDevice_ID INT UNIQUE,
    Latitude    FLOAT,
    Longitude   FLOAT,
    Status      VARCHAR(20)
);

CREATE TABLE IF NOT EXISTS RawData (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    AQDevice_ID INT,
    PM25 FLOAT,
    PM10 FLOAT,
    O3 FLOAT,
    NO2 FLOAT,
    SO2 FLOAT,
    CO FLOAT,
    DateTime DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (AQDevice_ID) REFERENCES AQDevices(AQDevice_ID)
);

CREATE TABLE IF NOT EXISTS AQIResults (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    AQDevice_ID INT,
    PM25 FLOAT,
    PM10 FLOAT,
    O3 FLOAT,
    NO2 FLOAT,
    SO2 FLOAT,
    CO FLOAT,
    DateTime DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (AQDevice_ID) REFERENCES AQDevices(AQDevice_ID)
);